    public const BUCKET_ORDER = ['1.12.2' => 0, '1.14.x' => 1, '1.16.x' => 2, '1.17.x' => 3, '1.18.x' => 4];

    /** @return array<int,array{id:string,name:string}> */
    public static function entitiesForProfile(string $profile, string $versionLabel): array
    {
        if ($profile === 'A') {
            return array_map(fn ($r) => ['id' => $r[0], 'name' => $r[1]], self::ENTITIES_PROFILE_A);
        }
        $maxRank = self::BUCKET_ORDER[$versionLabel] ?? self::BUCKET_ORDER['1.18.x'];
        return array_values(array_filter(array_map(
            fn ($r) => (self::BUCKET_ORDER[$r[2]] ?? 0) <= $maxRank ? ['id' => $r[0], 'name' => $r[1]] : null,
            self::ENTITIES_PROFILE_B
        )));
    }

    /** @return array<int,array{id:string,name:string,metadataLegend:?string}> */
    public static function itemsForProfile(string $profile, string $versionLabel): array
    {
        if ($profile === 'A') {
            return array_map(fn ($r) => ['id' => $r[0], 'name' => $r[1], 'metadataLegend' => $r[2]], self::ITEMS_PROFILE_A);
        }
        $maxRank = self::BUCKET_ORDER[$versionLabel] ?? self::BUCKET_ORDER['1.18.x'];
        return array_values(array_filter(array_map(
            fn ($r) => (self::BUCKET_ORDER[$r[2]] ?? 0) <= $maxRank ? ['id' => $r[0], 'name' => $r[1], 'metadataLegend' => null] : null,
            self::ITEMS_PROFILE_B
        )));
    }

    /** Raw export for grammar.php — client filters by profile/version itself to avoid a round-trip per version change. */
    public static function toArray(): array
    {
        return [
            'bucketOrder' => self::BUCKET_ORDER,
            'entitiesA' => self::ENTITIES_PROFILE_A,
            'entitiesB' => self::ENTITIES_PROFILE_B,
            'itemsA' => self::ITEMS_PROFILE_A,
            'itemsB' => self::ITEMS_PROFILE_B,
        ];
    }
